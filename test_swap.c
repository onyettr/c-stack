int test_swap ( void )
{
  stack_t *sp = NULL;  
  stack_t *sp1= NULL;
  
  printf("test_swap - create <int> stack\n");

  sp = StackCreate(4);
  sp1= StackCreate(4);
  
  (void)push(sp,100);
  (void)push(sp,200);
  (void)push(sp,300);
  (void)push(sp,700);

  (void)push(sp1,101);
  (void)push(sp1,201);
  (void)push(sp1,301);
  (void)push(sp1,701);

  StackDump(sp, 0);
  
  printf("\tTest01 swap - same size\n");
  (void)StackDestroy(sp);
  swap(sp, sp1);

  printf("sp1\n");
  StackDump(sp1, 0);
  printf("sp\n");  
  StackDump(sp, 0);
  
  printf("test_swap - Ends\n");

  return 0;
}

